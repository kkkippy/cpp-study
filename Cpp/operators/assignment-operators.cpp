#include <iostream>

// Operadores de atribuição (=, +=, -=, *=, /=, %=)

int main () {
    // Simples atribuição
    int x = 5;

    // Atribuição de adição
    int x = 5;
    x += 3; // x recebe 8 (5 + 3)

    // Atribuição de subtração
    int x = 5;
    x -= 3; // x recebe 2 (5 - 3)

    // Atribuição de multiplicação
    int x = 5;
    x *= 3; // x recebe 15 (5 * 3)

    // Atribuição de divisão
    int x = 15;
    x /= 3; // x recebe 5 (15 / 3)

    // Atribuição de módulo
    int x = 7;
    x %= 3; // x recebe 1 (7 % 3)

    return 0;
}