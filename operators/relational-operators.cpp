#include <iostream>

// Operadores relacionais/de comparação (==, !=, <, >, <=, >=)

int main () {
    bool isEqual = 5 == 3; // isEqual vai ser false

    bool isNotEqual = 5 != 3; // isNotEqual vai ser true

    bool isLess = 5 < 3; // isLess vai ser false

    bool isGreater = 5 > 3; // isGreater vai ser true

    bool isLessOrEqual = 5 <= 5; // isLessOrEqual vai ser false

    bool isGreaterOrEqual = 5 >= 3; // isGreaterOrEqual vai ser true

    return 0;
}